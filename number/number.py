print(min(4, 9, 0, 93, 76))

# round() roundoff a floating point number
print(round(3.5))
print(round(5.3))
#floor() returns the lowest value
print(floor(2.9))

#celi() roundoff to a possible decimal number
print(ceil(3.4))

